<h1>Pathogenesis</h1>
<p>&nbsp;&nbsp;&nbsp;&nbsp;Hepatocellular carcinoma, like any other cancer, develops when there is a mutation to the cellular machinery that causes the cell to replicate at a higher rate and/or results in the cell avoiding apoptosis. In particular, chronic infections of hepatitis B and/or C can aid the development of hepatocellular carcinoma by repeatedly causing the body's own immune system to attack the liver cells, some of which are infected by the virus, others merely bystanders. While this constant cycle of damage followed by repair can lead to mistakes during repair which in turn lead to carcinogenesis, this hypothesis is more applicable, at present, to hepatitis C. Chronic hepatitis C causes HCC through the stage of cirrhosis. In chronic hepatitis B, however, the integration of the viral genome into infected cells can directly induce a non-cirrhotic liver to develop HCC. Alternatively, repeated consumption of large amounts of ethanol can have a similar effect. Besides, cirrhosis is commonly caused by alcoholism, chronic hepatitis B and chronic hepatitis C. The toxin aflatoxin from certain Aspergillus species of fungus is a carcinogen and aids carcinogenesis of hepatocellular cancer by building up in the liver. The combined high prevalence of rates of aflatoxin and hepatitis B in settings like China and West Africa has led to relatively high rates of heptatocellular carcinoma in these regions. Other viral hepatitides such as hepatitis A have no potential to become a chronic infection and thus are not related to hepatocellular carcinoma.
</p>

