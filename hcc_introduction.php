<h1>Hepatocellular carcinoma</h1>
<p>&nbsp;&nbsp;&nbsp;&nbsp;Hepatocellular carcinoma (HCC, also called malignant hepatoma) is the most common type of liver cancer. Most cases of HCC are secondary to either a viral hepatitis infection (hepatitis B or C) or cirrhosis (alcoholism being the most common cause of hepatic cirrhosis).
Treatment options of HCC and prognosis are dependent on many factors but especially on tumour size and staging. Tumour grade is also important. High-grade tumours will have a poor prognosis, while low-grade tumors may go unnoticed for many years, as is the case in many other organs.

HCC is a relatively uncommon cancer in the United States. In countries where hepatitis is not common, most cancers of the liver are not primary HCC but metastasis (cancers spread from elsewhere in the body such as the colon).<br /><br />

