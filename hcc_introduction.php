<p>&nbsp;&nbsp;&nbsp;&nbsp;Hepatocellular carcinoma may present with yellow skin, bloating from fluid in the abdomen, easy bruising from blood clotting abnormalities, loss of appetite, unintentional weight loss, abdominal pain especially in the right upper quadrant, nausea, vomiting, or feeling tired
